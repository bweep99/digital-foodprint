                <h2>About this website</h2>
                <p>In today’s modern era, many Indonesians especially the younger generation have lost awareness of the long journey food takes before reaching their plates. The convenience of instant meals and a consumerist lifestyle have weakened the connection between society and agriculture. Despite being an agrarian country, Indonesia still imports essential food items such as rice, sugar, and salt, spending around IDR 1.272 trillion over the past 11 years. This highlights the urgent need to rebuild awareness and strengthen the relationship between the public and the agricultural sector.
                </p>
                <p>As a solution, Digital Foodprint was created—an interactive and educational platform that reconnects people with agriculture through visual animations, games focusing on local crops, and modules featuring stories from farmers. While the public can learn in a fun and engaging way, farmers and local contributors can connect and build business relationships through communication features. Developed with HTML, CSS, Python, and C++, the platform ensures a smooth and meaningful user experience. Digital Foodprint aims to grow collective awareness of the value of food and promote sustainable agriculture for Indonesia’s future.</p>
